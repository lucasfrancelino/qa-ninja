class PratoDoDia

    def prato_dia(dia)
        if dia == 'segunda feira'
            'Virado a Paulista'
        elsif dia == 'terça feira'
            'Dobradinha'
        elsif dia == 'quarta feira'
            'Feijoada' 
        elsif dia == 'quinta feira'
            'Macarronada'
        elsif dia == 'sexta feira'
            'Filé de merluza'
        elsif dia == 'sábado'
            'Veja o cardapio'
        elsif dia == 'domingo'
            'Fechado'
        else 
            'Dia inválido'
        end
    end


end